def get_strings(lang: str = 'en') -> dict:
    """Return the translation dict for *lang*, falling back to English."""
    return STRINGS.get(lang, STRINGS['en'])


def get_col_labels(lang: str = 'en') -> dict:
    """Return {col_name: display_label} for the given language."""
    S = get_strings(lang)
    return {col: S[key] for col, key in COL_KEY_MAP.items()}


def get_duration_labels(lang='en'):
    """Return list of translated duration display strings."""
    S = get_strings(lang)
    return [S[d[0]] for d in DURATION_OPTIONS]
